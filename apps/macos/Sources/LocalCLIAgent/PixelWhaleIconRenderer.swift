    private static func rows(_ rows: String...) -> [String] {
        rows
    }

    private static func makeImage(from rows: [String]) -> NSImage {
        let image = NSImage(size: iconSize)
        image.lockFocus()
        defer {
            image.unlockFocus()
            image.isTemplate = true
        }

        NSColor.clear.setFill()
        NSRect(origin: .zero, size: iconSize).fill()
        NSColor.black.setFill()
        NSGraphicsContext.current?.imageInterpolation = .none
        NSGraphicsContext.current?.shouldAntialias = false

        let pixelWidth = iconSize.width / CGFloat(rows.first?.count ?? 1)
        let pixelHeight = iconSize.height / CGFloat(rows.count)

        for (rowIndex, row) in rows.enumerated() {
            for (columnIndex, value) in row.enumerated() where value != "." {
                let rect = NSRect(
                    x: CGFloat(columnIndex) * pixelWidth,
                    y: iconSize.height - CGFloat(rowIndex + 1) * pixelHeight,
                    width: pixelWidth,
                    height: pixelHeight
                )
                rect.fill()
            }
        }

        return image
    }
